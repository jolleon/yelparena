Map.prototype.canMove = function(x, y) {

	// check x coordinate for bounds
	if ((x < 0) || (x >= this.array.length)) {
		return false
	}

	// check y coordinates for bounds
	if ((y < 0) || (y >= this.array[0].length)) {
		return false
	}

	if (this.array[x][y] === 1) {
		return false
	}

	return true
}


